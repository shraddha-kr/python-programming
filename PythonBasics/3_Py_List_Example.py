# Python program to illustrate a list

"""
Python have 4 types of built in Data Structures namely List,
Dictionary, Tuple and Set.List is the most basic Data Structure
in python. List is a mutable data structure i.e
items can be added to list later after the list creation.
"""

# Creates an empty list
nums = []

# appending data in list, append() function is used to add data to the list.
nums.append(21)
nums.append(40.5)
nums.append("String")

# print a blank line
print()

# print the contents of the list
print(nums)

